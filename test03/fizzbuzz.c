#include<unistd.h>

int	main(void)
{
	int	i;
	char	j;

	i = 1;
	while (i <= 100)
	{
		if (i % 3 == 0)
			write(1, "fizz", 4);
		if (i % 5 == 0)
			write(1, "buzz", 4);
		else if (i % 3 != 0 && i % 5 != 0)
		{
			if (i > 9)
			{
				j = (i / 10) + '0';
				write(1, &j, 1);
			}
			j = (i % 10) + '0';
			write(1, &j, 1);
		}
		write(1, "\n", 1);
		i++;
	}
	return (0);
}
