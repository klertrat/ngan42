#include<unistd.h>

void	ft_putnbr(int nb)
{
	int	i;
	int	r[11];

	i = 0;
	if (nb == 0)
		write (1, "0", 1);
	if (nb == -2147483648)
	{
		write (1, "-2147483648", 11);
	}
	if (nb < 0 && nb != -2147483648)
	{
		write (1, "-", 1);
		nb = nb * -1;
	}
	while (nb > 0)
	{
		r[i] = (nb % 10) + '0';
		nb = nb / 10;
		i++;
	}
	while (--i >= 0)
	{
		write (1, &r[i], 1);
	}
}
