#include<unistd.h>

int	main(int argc, char **argv)
{
	int	j;

	(void)argc;
	j = 0;
	if (argc != 2)
		write(1, "a", 1);
	else
	{
		while (argv[1][j] != '\0')
		{
			if (argv[1][j] == 'a')
				write(1, "a", 1);
			j++;
		}
	}
	write(1, "\n", 1);
}
